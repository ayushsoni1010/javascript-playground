// Array and Array methods Big-O complexity

// Insert / remove from end => O(1) - Constant
// Insert / remove from beginning => O(n) - Linear
// Access => O(1) - Constant
// Search => O(n) - Linear

Array.push(); // => O(1) - Constant
Array.pop(); // => O(1) - Constant

Array.shift(); // => O(n) - Linear
Array.unshift(); // => O(n) - Linear
Array.concat(); // => O(n) - Linear
Array.slice(); // => O(n) - Linear
Array.splice(); // => O(n) - Linear

Array.forEach(); // => O(n) - Linear
Array.map(); // => O(n) - Linear
Array.filter(); // => O(n) - Linear
Array.reduce(); // => O(n) - Linear

/* ********************************************************************************************* */

// Object and Object methods Big-O complexity

// Insert => O(1) - Constant
// Remove => O(n) - Constant
// Access => O(1) - Constant
// Search => O(n) - Linear

Object.keys(); // => O(n) - Linear
Object.values(); // => O(n) - Linear
Object.entries(); // => O(n) - Linear

